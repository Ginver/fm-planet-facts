export const getStaticPaths = async () => {
  const response = await contentfulClient.getEntries({
    content_type: "planet",
  });

  const paths = response.items.map((planet) => {
    return {
      params: { planetName: planet.fields.name.toLowerCase() },
    };
  });
  console.log("What is PATHS?", paths);
  return {
    paths: paths,
    fallback: false, // false or "blocking"
  };
};

